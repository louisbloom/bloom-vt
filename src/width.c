static int range_lookup(const Range *table, size_t n, uint32_t cp)
{
    size_t lo = 0, hi = n;
    while (lo < hi) {
        size_t mid = lo + (hi - lo) / 2;
        if (cp < table[mid].lo)
            hi = mid;
        else if (cp > table[mid].hi)
            lo = mid + 1;
        else
            return 1;
    }
    return 0;
}

#define ARRAY_LEN(a) (sizeof(a) / sizeof((a)[0]))

int bvt_codepoint_width(BvtTerm *vt, uint32_t cp)
{
    /* ASCII fast path */
    if (cp < 0x7Fu) {
        if (cp < 0x20u)
            return 0;
        return 1;
    }
    if (cp < 0xA0u)
        return 0; /* DEL + C1 */
    if (cp == 0x00ADu)
        return 0; /* soft hyphen */

    if (range_lookup(ZERO, ARRAY_LEN(ZERO), cp))
        return 0;
    if (range_lookup(WIDE, ARRAY_LEN(WIDE), cp))
        return 2;

    /* Ambiguous: TODO once UCD-derived table is generated. With the
     * default (not-wide), all unclassified codepoints fall through as 1. */
    (void)vt;
    return 1;
}

/* UAX #29-style helpers — small subset sufficient for emoji clusters. */

static bool is_extend(uint32_t cp)
{
    /* GB9: Extend ∪ ZWJ ∪ format (here we approximate with ZERO ranges). */
    if (cp == 0x200Du)
        return true; /* ZWJ */
    if (cp >= 0x0300u && cp <= 0x036Fu)
        return true; /* combining */
    if (cp >= 0xFE00u && cp <= 0xFE0Fu)
        return true; /* variation selectors */
    if (cp >= 0xFE20u && cp <= 0xFE2Fu)
        return true;
    if (cp >= 0xE0100u && cp <= 0xE01EFu)
        return true; /* VS supp */
    if (cp >= 0xE0020u && cp <= 0xE007Fu)
        return true; /* tag chars */
    if (cp >= 0x1F3FBu && cp <= 0x1F3FFu)
        return true; /* skin tones */
    /* Conservative fallback: anything classified zero-width attaches. */
    return range_lookup(ZERO, ARRAY_LEN(ZERO), cp) != 0;
}

static bool is_regional_indicator(uint32_t cp)
{
    return cp >= 0x1F1E6u && cp <= 0x1F1FFu;
}

static bool has_vs16(const uint32_t *cps, uint32_t len)
{
    for (uint32_t i = 0; i < len; ++i)
        if (cps[i] == 0xFE0Fu)
            return true;
    return false;
}

